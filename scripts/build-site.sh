SITE_DIR="${1:-$ROOT_DIR/artifacts/site}"
VISUAL_DIR="${2:-$ROOT_DIR/artifacts/visual}"
VALIDATION_DIR="${3:-$ROOT_DIR/artifacts/validation}"

